# Below is a list of useful alias commands
# Search a command in history
alias gh='history | grep'

# Find out which files you worked on last
alias left='ls -t -1'

# Shorten python3 to py
alias py='python3'

# Show todays
alias today='date +"%A, %B %-d, %Y"'

# Alias sudo
alias sudo='sudo '

# Update system
alias sysupdate='apt update && apt upgrade'

# Change cat to pr
alias pr='cat'

# Shorten jekyll commands
alias jekylls='bundle exec jekyll serve'
alias jekyllb='bundle exec jekyll build'
alias jekylld='bundle exec jekyll serve --drafts'
alias jekyllc='bundle exec jekyll clean'

# Disable dd command
alias dd='echo "sorry the dd command is disabled"'
